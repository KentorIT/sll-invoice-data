package se.sll.invoicedata.core.access;

public class OperationAccessKey {
	
	private String key;
	
	@SuppressWarnings("unused")
	private OperationAccessKey() {
		super();
	}
	
	public OperationAccessKey(Operation operationEnum, String hsaId) {
		key = operationEnum.name() + hsaId;
	}
	
	public String getKey() {
		return key;
	}

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((key == null) ? 0 : key.hashCode());
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		OperationAccessKey other = (OperationAccessKey) obj;
		if (key == null) {
			if (other.key != null)
				return false;
		} else if (!key.equals(other.key))
			return false;
		return true;
	}	
	
	

}
